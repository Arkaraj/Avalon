<h1 id="avalon-instructions">Avalon Instructions</h1>
<p>Avalon is an extension to assign tasks to members of a club/chapter/rooms, by creating rooms.</p>
<h2 id="authentication">Authentication</h2>
<p>The authentication is through GitHub.</p>
<p>Once the user clicks on login, it will redirect to the preferred web browser to login. Once signed up you will be shown the dashboard.</p>
<h2 id="dashboard">Dashboard</h2>
<p>This is like the home Screen or the home page, it will display the Rooms created by the user, and the rooms in which the users are members. This will also have the logout button, and have key room informations and functions like the room code and option to leave the room.</p>
<p>The dashboard also contains the user&#39;s github Id, which will be useful to add the user as admins in other rooms.</p>
<p>The user can navigate back and forth between rooms through this dashboard.</p>
<h2 id="creating-rooms">Creating Rooms</h2>
<p>Creating rooms are very simple, there is a button in the dashboard which says create room, by clicking it, vs code will ask the user to enter some room details:</p>
<p>Name of the Room *</p>
<p>Description of the room (can be updated later also) *.</p>
<ul>
<li>* - leave it blank</li>
</ul>
<h3 id="admin">Admin</h3>
<p>Once the room is created, you are the admin, the room will give a unique code of 6 characters, share this code with people you want to add in the room.
You can see the members in the room, assign them tasks by inputing the task and hitting enter, also the completed tasks by the members will be striked off, and also add new admins, by entering their github ids.</p>
<h2 id="joining-rooms">Joining Rooms</h2>
<p>Joining room is as easy as a pie, just by entering the room code given by the admin, you can enter the rooms.</p>
<h2 id="tasks">Tasks</h2>
<p>There is a separate tasks panel also, where the user can view all the tasks(from every room).</p>
<p>The users can check off the tasks that they have completed.</p>
